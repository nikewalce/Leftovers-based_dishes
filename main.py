# Программа для генерации меню из доступных продуктов

# Импорт библиотеки для работы со словарями и списками
import collections

# Словарь для хранения информации о продуктах
products = {
    "Яйца": 10,
    "Молоко": 500,
    "Мука": 250,
    "Сахар": 300,
    "Яблоки": 4,
    "Картофель": 6,
}

# Функция для проверки наличия продуктов
def check_products():
    # Проверка наличия каждого продукта
    for product, quantity in products.items():
        if quantity <= 0:
            return False
    return True

# Генерация меню
def generate_menu():
    # Если продукты есть в наличии, генерируем меню
    if check_products():
        # Создание списка возможных блюд
        menu = [
            {
                "Название": "Омлет",
                "Ингредиенты": ["Яйца"],
                "Инструкция": "Взбить яйца, обжарить на сковороде."
            },
            {
                "Название": "Яблочный пирог",
                "Ингредиенты": ["Яблоки", "Мука", "Сахар"],
                "Инструкция": "Смешать муку, сахар, добавить нарезанные яблоки, выпекать."
            },
            {
                "Название": "Картофельное пюре",
                "Ингредиенты": ["Картофель", "Молоко"],
                "Инструкция": "Отварить картофель, размять, добавить молоко."
            }
        ]
        return menu
    else:
        print("Недостаточно продуктов для приготовления блюд.")

def find_missing_ingredient(ingredients):  # функция для поиска недостающего ингредиента
    missing_product = None
    for ingredient in ingredients:
        if products[ingredient] <= 0:  # проверяем наличие ингредиента в словаре
            missing_product = ingredient
            break  # как только нашли недостающий ингредиент, выходим из цикла
    return missing_product  # возвращаем недостающий ингредиент или None, если всё в порядке

# Вывод меню
menu = generate_menu()
if menu:
    print(menu[0]['Название'], menu[0]['Ингредиенты'], menu[0]['Инструкция'])

    for i in menu[0]['Ингредиенты']:
        if i in products:
            print(i)

else:
    print("Проверьте наличие продуктов и попробуйте снова.")
